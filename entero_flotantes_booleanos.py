# Enteros, Flotantes  Booleanos
x = 10
y = 5.123
z = 1.2E6
# En Python, la notación científica es una forma conveniente de manejar 
# números muy grandes o muy pequeños de manera compacta y precisa.
print("=> Tipo de dato: ",type(x))
print("=> Tipo de dato: ",type(y))
print("=> Tipo de dato: ",type(z))

# Los tipos de datos booleanos son fundamentales para la lógica 
# de programación, permitiendo que los programas tomen decisiones 
# y ejecuten diferentes acciones basadas en condiciones verdaderas o falsas.
is_true = True
is_false = False
print("=> Tipo de dato: ",type(is_true))
print("=> Tipo de dato: ",type(is_false))